#!/usr/bin/env node
//
// Generate the GitHub Wiki "Reference: Database Schema" page from the Drizzle
// schema. Table summaries come from each pgTable's TSDoc; columns (type, PK/FK/
// not-null/default + inline rationale notes) are parsed straight from schema.ts.
// Anchors use the shared ghSlug so the jump-nav links resolve on GitHub.
//
//   node .claude/skills/code-readability/scripts/gen-schema-page.mjs [wiki-dir]
//
// Writes <wiki-dir>/Reference-Database-Schema.md (default wiki-dir: /tmp/cr-wiki).
//
// Config: the schema path defaults to `process.env.CR_SCHEMA || 'src/db/schema.ts'`.
// The entity-relationship diagram and the "AI context" callout below are
// PROJECT-SPECIFIC — they ship as a minimal generic template you customize once
// (search for `// TODO`). The table/column extraction itself is fully generic.
//
import fs from 'node:fs';
import { ghSlug } from './wiki-slug.mjs';

const SCHEMA = process.env.CR_SCHEMA || 'src/db/schema.ts';
const OUT = process.argv[2] || '/tmp/cr-wiki';

const src = fs.readFileSync(SCHEMA, 'utf8').split('\n');
const TYPE = { uuid: 'uuid', text: 'text', integer: 'integer', boolean: 'boolean', timestamp: 'timestamp', decimal: 'numeric', date: 'date', jsonb: 'jsonb' };
const clean = (s) => String(s ?? '').replace(/\s+/g, ' ').replace(/\|/g, '\\|').trim();

// Pass 1: symbol → SQL table name (to resolve FK targets to their table name).
const symToSql = {};
for (const l of src) { const m = l.match(/^export const (\w+) = pgTable\('([^']+)'/); if (m) symToSql[m[1]] = m[2]; }

const tables = [];
for (let i = 0; i < src.length; i++) {
  const m = src[i].match(/^export const (\w+) = pgTable\('([^']+)',\s*\{/);
  if (!m) continue;
  const [, symbol, sqlName] = m;
  // table summary from the adjacent /** */
  let summary = '';
  if ((src[i - 1] || '').trim().endsWith('*/')) {
    let k = i - 1; const end = k;
    while (k >= 0 && !src[k].trim().startsWith('/**')) k--;
    summary = src.slice(k, end + 1).map((l) => l.replace(/^\s*\/\*\*/, '').replace(/\s*\*\/\s*$/, '').replace(/^\s*\*\s?/, '')).join(' ').replace(/\s+/g, ' ').trim();
  }
  // columns until the object closes
  const cols = [];
  let note = [];
  for (let j = i + 1; j < src.length; j++) {
    const l = src[j];
    if (/^\s*\}/.test(l)) break;
    const c = l.match(/^\s*(\w+):\s*(\w+)\(\s*'([^']+)'/);
    if (!c) { const cm = l.match(/^\s*\/\/\s?(.*)$/); if (cm) note.push(cm[1]); continue; }
    const [, name, fn, sqlCol] = c;
    let type = TYPE[fn] || fn;
    if (fn === 'decimal') { const p = l.match(/precision:\s*(\d+),\s*scale:\s*(\d+)/); if (p) type = `numeric(${p[1]},${p[2]})`; }
    if (fn === 'timestamp' && /withTimezone:\s*true/.test(l)) type = 'timestamptz';
    const cons = [];
    if (/\.primaryKey\(\)/.test(l)) cons.push('**PK**');
    const fk = l.match(/\.references\(\(\)\s*=>\s*(\w+)\.(\w+)/); if (fk) cons.push(`FK → \`${symToSql[fk[1]] || fk[1]}.${fk[2]}\``);
    if (/\.notNull\(\)/.test(l)) cons.push('not null');
    if (/\.defaultRandom\(\)/.test(l)) cons.push('default random');
    else if (/\.defaultNow\(\)/.test(l)) cons.push('default now');
    else { const d = l.match(/\.default\(([^)]*)\)/); if (d) cons.push(`default \`${d[1].replace(/['"`]/g, '')}\``); }
    const trail = l.match(/,\s*\/\/\s?(.*)$/); if (trail) note.push(trail[1]);
    let notes = cons.join(' · ');
    if (note.length) { const r = clean(note.join(' ')); notes += (notes ? ' — ' : '') + (r.length > 150 ? `${r.slice(0, 149)}…` : r); }
    cols.push({ name, sqlCol, type, notes });
    note = [];
  }
  tables.push({ symbol, sqlName, summary, cols });
}

const section = (t) => {
  const head = '| Column | Type | Notes |\n|---|---|---|';
  const rows = t.cols.map((c) => `| \`${c.sqlCol}\` | \`${c.type}\` | ${c.notes || '—'} |`);
  return `### ${t.sqlName}\n\n${t.summary || '_No summary._'}\n\n${[head, ...rows].join('\n')}\n`;
};

const page = `<!-- generated by /code-readability — edits will be overwritten -->
# Reference: Database Schema

**Jump to:** ${tables.map((t) => `[${t.sqlName}](#${ghSlug(t.sqlName)})`).join(' · ')}

> **AI context:** <!-- TODO: describe your schema's core tables — the keystone
> table(s), any non-obvious modeling choices (e.g. polymorphic rows, tree
> structures), and where the source of truth lives. Table summaries below come
> from each table's TSDoc; columns are parsed from the schema source. For the
> conceptual model see [[Architecture]]; for the narrative data dictionary see
> [[Data-Model]]. -->

## Entity relationships

<!-- TODO: replace this placeholder ER diagram with your own. Use Mermaid
     erDiagram syntax: \`tableA ||--o{ tableB : "relationship"\`. -->
\`\`\`mermaid
erDiagram
  parent_table ||--o{ child_table : "has many"
  child_table  ||--o{ join_table  : "linked via"
\`\`\`

## Tables

${tables.map(section).join('\n')}
---

_Generated from \`${SCHEMA}\` (${tables.length} tables). See also:_ [[Reference-API]] · [[Architecture]] · [[Data-Model]]
`;
fs.writeFileSync(`${OUT}/Reference-Database-Schema.md`, page);
console.log(`${OUT}/Reference-Database-Schema.md —`, page.length, 'bytes,', tables.length, 'tables,', tables.reduce((n, t) => n + t.cols.length, 0), 'columns');
